#include <stdio.h>
int main()
{
    printf("Logical Operators:\n");
    /*Logical operators are used to compare conditions to see whether they are satisfying or not*/
    /*It provides us the result in binary format "1" or "0"*/
    int x = 3;
    int y = 2;
    printf("x and y = %d", x || y);
    return 0;
}
